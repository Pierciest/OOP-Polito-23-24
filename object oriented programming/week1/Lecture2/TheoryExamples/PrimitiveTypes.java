package Lecture2.TheoryExamples;

public class PrimitiveTypes {
    public static void main(String[] args) {
        // Declaration and initialization of primitive types
        // when we declare, we declare the type and the name
        // each type has different memory sizes
        int age = 25;
        double height = 1.75;
        char gender = 'M';
        boolean isStudent = true;

        // Printing the values of primitive types
        System.out.println("Age: " + age);
        System.out.println("Height: " + height);
        System.out.println("Gender: " + gender);
        System.out.println("Is student: " + isStudent);
    }
}
